  #      Object Oriented Programming (OOP) in Python

# level 1: Class and Object

# A class is a blueprint for creating objects. An object is an instance of a class.
class student:    # Defining a class named 'student'
    name = "Ali"   # Class attribute 'name'
    age = 23


# Creating an object of the class student
obj1 = student()
print("Name:", obj1.name) # Accessing the class attribute 'name' using the object
print("Age:", obj1.age)



